package Modelo;

public class RHotel {

	String codSucursal;
	String codH;
	String nombre;
	String ciudad;
	String numEstrellas;
	String regimen;
	String claseHabitacion;
	String disponible;
	String nombreCliente;
	String dniCliente;
	String tlfCliente;
	String fechaE;
	String fechaS;
	

	public RHotel() {
		codSucursal = "";
		codH = "";
		nombre = "";
		ciudad = "";
		numEstrellas = "";
		regimen = "";
		claseHabitacion = "";
		disponible = "";
		nombreCliente = "";
		dniCliente = "";
		tlfCliente = "";
		fechaE= "";
		fechaS = "";
	}


	public String getCodSucursal() {
		return codSucursal;
	}


	public void setCodSucursal(String codSucursal) {
		this.codSucursal = codSucursal;
	}


	public String getCodH() {
		return codH;
	}


	public void setCodH(String codH) {
		this.codH = codH;
	}


	public String getNombre() {
		return nombre;
	}


	public void setNombre(String nombre) {
		this.nombre = nombre;
	}


	public String getCiudad() {
		return ciudad;
	}


	public void setCiudad(String ciudad) {
		this.ciudad = ciudad;
	}


	public String getNumEstrellas() {
		return numEstrellas;
	}


	public void setNumEstrellas(String numEstrellas) {
		this.numEstrellas = numEstrellas;
	}


	public String getRegimen() {
		return regimen;
	}


	public void setRegimen(String regimen) {
		this.regimen = regimen;
	}


	public String getClaseHabitacion() {
		return claseHabitacion;
	}


	public void setClaseHabitacion(String claseHabitacion) {
		this.claseHabitacion = claseHabitacion;
	}


	public String getDisponible() {
		return disponible;
	}


	public void setDisponible(String disponible) {
		this.disponible = disponible;
	}


	public String getNombreCliente() {
		return nombreCliente;
	}


	public void setNombreCliente(String nombreCliente) {
		this.nombreCliente = nombreCliente;
	}


	public String getDniCliente() {
		return dniCliente;
	}


	public void setDniCliente(String dniCliente) {
		this.dniCliente = dniCliente;
	}


	public String getTlfCliente() {
		return tlfCliente;
	}


	public void setTlfCliente(String tlfCliente) {
		this.tlfCliente = tlfCliente;
	}


	public String getFechaE() {
		return fechaE;
	}


	public void setFechaE(String fechaE) {
		this.fechaE = fechaE;
	}


	public String getFechaS() {
		return fechaS;
	}


	public void setFechaS(String fechaS) {
		this.fechaS = fechaS;
	}
	
	

}
